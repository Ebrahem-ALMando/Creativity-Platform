    {
        try {
            $branch=$request->get('branchName');
            $year=$request->get('yearName');
            $subjects=subject::where('branch',$branch)->where('year',$year)->get();
            return response()->json($subjects);

        }
        catch (\Exception $exception){
            Log::error($exception);
        }

    }

